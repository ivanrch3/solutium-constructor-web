// Helper to get business image
export const getBusinessImage = (type: string) => {
  const t = type.toLowerCase();
  
  // Direct match for categories
  if (t in PEXELS_IMAGES) {
    const images = PEXELS_IMAGES[t as keyof typeof PEXELS_IMAGES];
    return images[Math.floor(Math.random() * images.length)];
  }

  let category: keyof typeof PEXELS_IMAGES = 'general';

  if (t.includes('agricultura') || t.includes('ganadería') || t.includes('campo') || t.includes('granja')) {
    category = 'agriculture';
  } else if (t.includes('comida') || t.includes('restaurante') || t.includes('chef') || t.includes('gastronomía') || t.includes('bebida') || t.includes('alimentación')) {
    category = 'food';
  } else if (t.includes('arte') || t.includes('diseño') || t.includes('creativo') || t.includes('pintura')) {
    category = 'art';
  } else if (t.includes('automotriz') || t.includes('coche') || t.includes('auto') || t.includes('taller')) {
    category = 'automotive';
  } else if (t.includes('salud') || t.includes('clínica') || t.includes('doctor') || t.includes('dental') || t.includes('medicina')) {
    category = 'health';
  } else if (t.includes('tecnología') || t.includes('software') || t.includes('app') || t.includes('digital') || t.includes('web')) {
    category = 'tech';
  } else if (t.includes('finanzas') || t.includes('banco') || t.includes('contabilidad') || t.includes('seguro')) {
    category = 'finance';
  } else if (t.includes('legal') || t.includes('abogado') || t.includes('ley')) {
    category = 'legal';
  } else if (t.includes('deporte') || t.includes('gym') || t.includes('fitness') || t.includes('entrenador')) {
    category = 'sports';
  } else if (t.includes('arquitectura') || t.includes('construcción') || t.includes('reforma') || t.includes('inmobiliaria') || t.includes('bienes raíces') || t.includes('casa')) {
    category = 'realestate';
  } else if (t.includes('influencer') || t.includes('blog') || t.includes('personal')) {
    category = 'influencer';
  } else if (t.includes('ecommerce') || t.includes('comercio electrónico') || t.includes('tienda online')) {
    category = 'ecommerce';
  } else if (t.includes('consultoría') || t.includes('coaching') || t.includes('asesor')) {
    category = 'consulting';
  } else if (t.includes('educación') || t.includes('escuela') || t.includes('curso')) {
    category = 'education';
  } else if (t.includes('entretenimiento') || t.includes('evento') || t.includes('fiesta') || t.includes('música')) {
    category = 'entertainment';
  } else if (t.includes('fotografía') || t.includes('video') || t.includes('cámara')) {
    category = 'photography';
  } else if (t.includes('marketing') || t.includes('publicidad') || t.includes('social media')) {
    category = 'marketing';
  } else if (t.includes('mascota') || t.includes('veterinaria') || t.includes('perro') || t.includes('gato')) {
    category = 'pets';
  } else if (t.includes('moda') || t.includes('ropa') || t.includes('textil')) {
    category = 'fashion';
  } else if (t.includes('ong') || t.includes('sin fines de lucro') || t.includes('voluntariado') || t.includes('donación')) {
    category = 'ngo';
  } else if (t.includes('limpieza') || t.includes('mantenimiento')) {
    category = 'cleaning';
  } else if (t.includes('transporte') || t.includes('logística') || t.includes('envío') || t.includes('camión')) {
    category = 'logistics';
  } else if (t.includes('viaje') || t.includes('turismo') || t.includes('hotel') || t.includes('hospitalidad')) {
    category = 'travel';
  } else if (t.includes('belleza') || t.includes('spa') || t.includes('estética') || t.includes('cosmética') || t.includes('uñas')) {
    category = 'beauty';
  } else if (t.includes('servicio profesional') || t.includes('oficina') || t.includes('trabajo')) {
    category = 'professional';
  }

  // Pick a random image from the selected category
  const images = PEXELS_IMAGES[category];
  return images[Math.floor(Math.random() * images.length)];
};
